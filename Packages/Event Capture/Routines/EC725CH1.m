NAMECHG ;* Change the names of National Procedures
 D:$P($T(CHNG+1),";;",2)'="QUIT" CHANGE
 Q
 ;
CHANGE ;** Change National Procedure names
 ;
 ;  ECXX is in format:
 ;   NATIONAL NUMBER ^ NEW NAME ^ CPT CODE ^ OLD NAME
 ;
 N ECX,ECXX,ECDA
 D MES^XPDUTL(" ")
 D BMES^XPDUTL(" Changing National Procedure names in National Procedure File (#725)...")
 F ECX=1:1 K DD,DO,DA S ECXX=$P($T(CHNG+ECX),";;",2) Q:ECXX="QUIT"  DO
 .S ECDA=+$O(^EC(725,"D",$P(ECXX,U,1),0))
 .I $D(^EC(725,ECDA,0)) DO
 ..K ^EC(725,"B",$P(^EC(725,ECDA,0),"^",1),ECDA)
 ..S $P(^EC(725,ECDA,0),"^",1)=$P(ECXX,U,2)
 ..S DA=ECDA,DIK(1)=".01",DIK="^EC(725,"
 ..D EN^DIK,MESC
 K DIK,DA
 Q
 ;
MESC ;** Change message
 N ECCMSG,ECCMSG1
 S ECCMSG="    ..."_$P(ECXX,U,4)_" ("_$P(ECXX,U,1)_")"
 S ECCMSG1="          changed to "_$P(ECXX,U,2)_"..."
 D BMES^XPDUTL(ECCMSG)
 D MES^XPDUTL(ECCMSG1)
 Q
