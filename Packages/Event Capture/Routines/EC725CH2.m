ADDPROC ;* Add National Procedures
 D:$P($T(NEW+1),";;",2)'="QUIT" ADD
 Q
 ;
ADD ;** Add National Procedures
 ;
 ;  ECXX is in format:
 ;   NATIONAL NUMBER ^ NAME ^ CPT CODE
 ;
 N ECX,ECXX,ECDA
 D MES^XPDUTL(" ")
 D BMES^XPDUTL(" Adding new National Procedures to EC National Procedure File (#725)...")
 D MES^XPDUTL(" ")
 ;
 S ECDA=325
 F ECX=1:1 S ECXX=$P($T(NEW+ECX),";;",2) Q:ECXX="QUIT"  DO
 .S ECDA=ECDA+1
 .S ^EC(725,ECDA,0)=$P(ECXX,"^",2)_"^"_$P(ECXX,"^",1)_"^^^"_$P(ECXX,"^",3)
 .S DA=ECDA,DIK(1)=".01",DIK="^EC(725,"
 .D EN^DIK
 .K DA,DIK
 .S DA=ECDA,DIK(1)="1",DIK="^EC(725,"
 .D EN^DIK,MESS
 .K DA,DIK
 .S $P(^EC(725,0),"^",4)=$P(^EC(725,0),"^",4)+1
 Q
 ;
MESS ;** Add message
 N ECXADMSG
 S ECXADMSG="    ..."_$P(ECXX,"^",2)_" ("_$P(ECXX,"^",1)_") added..."
 D BMES^XPDUTL(ECXADMSG)
 Q
