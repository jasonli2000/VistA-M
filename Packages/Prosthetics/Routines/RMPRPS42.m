 ;
KILL K I,J,RMIEN,RMPAT,RMSCAT,RMFRM,RMDAT0,RMDATAM,RMCHK,RMTYP,RMGRP,RMSRC
 K RMDAT,RMHC,DA,RMDES,X,DIC,DLAYGO,R0,RH,RI,RIT,RMHCPC,RMIT,RLO,RHC
