 S BN=0,DMCBT=0
 F  S BN=$O(^PRCA(430,"C",DEBTOR,BN)) Q:'BN  D
   . Q:'$D(^PRCA(430,BN,12))
   . F DMC=1:1:4 S DMCBD(DMC)=$P(^PRCA(430,BN,12),U,DMC)
   . S DMCAMT=0
   . F DMC=2:1:4 S DMCBT=DMCBT+DMCBD(DMC),DMCAMT=DMCAMT+DMCBD(DMC),DMCBT(DMC)=DMCBT(DMC)+DMCBD(DMC)
   . W !,BN,?8,DMCBD(1),?18,$J(DMCBD(2),12,2),$J(DMCBD(3),12,2),$J(DMCBD(4),12,2),$J(DMCAMT,12,2),$J(DMCBT,12,2)
 S TAMT=0
 W !,?18
 F DMC=2:1:5 W "  ----------"
 W !,?18
 F DMC=2:1:4 W $J(DMCBT(DMC),12,2) S TAMT=TAMT+DMCBT(DMC)
 W $J(TAMT,12,2)
