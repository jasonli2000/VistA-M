V ;;1.0;CLASS III REPORTS OCTOBER 15,1998
PRPTS ; PRINT ALL REPORTS
 D NOW^%DTC
 D:'$G(^XTMP("A4ARIG",$J,"BILL",0)) EN1^A4ARIG
 S DATE=^XTMP("A4ARIG",$J,"BILL","DATE/TIME",0)
 ; D OPEN
 W @IOF
 D BSRPTS
 D CLOSE
 D EOR
 QUIT
BSRPTS ; PRINT BILL STATUS REPORTS
 S ACCRUED=""
 F  S ACCRUED=$O(^XTMP("A4ARIG",$J,"ACCRUED",ACCRUED)) Q:ACCRUED=""  D
    .  D BSRPT1
    .  D BSRPT2
    .  D BSRPT3
    .  D BSRPT4
    .  D:ACCRUED=1 BSRPT5
 QUIT
BSRPT1 ;
 D ^A4ARIG1
 QUIT
BSRPT2 ;
 D ^A4ARIG2
 QUIT
BSRPT3 ;
 D ^A4ARIG3
 QUIT
BSRPT4 ;
 D ^A4ARIG4
 QUIT
BSRPT5 ;
 D ^A4ARIG5
 QUIT
OPEN ; SELECT OUTPUT DEVICE
 S %ZIS="M" D ^%ZIS U IO
 QUIT
CLOSE ; CLOSE OUTPUT DEVICE
 D ^%ZISC
 QUIT
EOR ; END OF ROUTINE
