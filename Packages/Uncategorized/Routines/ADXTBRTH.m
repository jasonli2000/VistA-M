ADXTBRTH ;523/KC 8/2/93 fill in local field with data, in 165.2 file
 ;;1.1;;
 ;
 ;N ADXZI,ADXZEN,ADXZMRS,ADXZX,DIE,DR,DA,DIC,DD,D0
 ;
 U IO(0) W !,"Filling in local field in the BIRTHPLACE FOR ONCOLOGY FILE...",!
 F ADXZI=1:1 S ADXZX=$T(DATA+ADXZI) Q:ADXZX']""  D
 .I '+(ADXZI#10) U IO(0) W "."
 .S ADXZX=$P(ADXZX,";;",2)
 .S ADXZEN=$P(ADXZX,"^",2),ADXZMRS=$P(ADXZX,"^")
 .Q:$D(^ONCO(165.2,"A523001",ADXZMRS))  ; quit if already built
 .; look up country
 .K DIC,DA,DR,DIE
 .S X=ADXZEN,DIC(0)="Z",DIC="^ONCO(165.2," D ^DIC Q:(+Y'>0)
 .; add MRS code to country's 523001 multiple
 .K DIE,DR,DA,DIC,DD,D0
 .S X=ADXZMRS,DA(1)=+Y,DIC="^ONCO(165.2,"_DA(1)_",523001,",DIC(0)="L"
 .I '$D(^ONCO(165.2,DA(1),523001)) D
 ..S ^ONCO(165.2,DA(1),523001,0)="^"_$P(^DD(165.2,523001,0),"^",2)
 .D FILE^DICN
EXIT ;
 K ADXZI,ADXZEN,ADXZMRS,ADXZX,DIE,DR,DA,DIC,DD,D0
 Q
