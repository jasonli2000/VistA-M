 ;;1.0;;
 N ADXTDTOP,ADXTPID,ADXTDSQ,D0,DA
 S ADXTDTOP=""
 F  S ADXTDTOP=$O(^TMP("ADXT","DI","OP",ADXTDTOP)) Q:ADXTDTOP']""  D
 .S ADXTPID=""
 .F  S ADXTPID=$O(^TMP("ADXT","DI","OP",ADXTDTOP,ADXTPID)) Q:ADXTPID']""  D
 ..S ADXTDSQ=""
 ..F  S ADXTDSQ=$O(^TMP("ADXT","DI","OP",ADXTDTOP,ADXTPID,ADXTDSQ)) Q:ADXTDSQ']""  D
 ...S (D0,DA)=^TMP("ADXT","DI","OP",ADXTDTOP,ADXTPID,ADXTDSQ)
 ...D YR^ADXTPCO8,ES^ADXTPCO8
EXIT ;
 K ADXTDTOP,ADXTPID,ADXTDSQ,D0,DA
 Q
