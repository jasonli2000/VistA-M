AFTLAC ;CLKS/SO CHECK LAC("LRAC" GLOBAL FOR ORPHANED .5 NODES 4/6/95
 ;;V1.0;;4/6/95
 Q:'$D(IOF)
 W @IOF
 N LRDFN,LRMH
 S LRDFN=0
 F  S LRDFN=$O(^LAC("LRAC",+LRDFN)) Q:+LRDFN<1  D
 . S LRMH=0
 . F  S LRMH=$O(^LAC("LRAC",+LRDFN,1,+LRMH)) Q:+LRMH<1  D
 .. I $D(^LAC("LRAC",+LRDFN,1,+LRMH,.5)),'$D(^LAC("LRAC",+LRDFN,1,+LRMH,0)) W "LRDFN=",LRDFN,"  LRMH=",LRMH,"  DFN=",$P(^LR(+LRDFN,0),"^",2,3),!
 .. Q
 . I $D(^LAC("LRAC",+LRDFN,"MISC",1,.5)),'$D(^LAC("LRAC",+LRDFN,"MISC",1,0)) W "LRDFN=",LRDFN,"  MISC  DFN=",$P(^LR(+LRDFN,0),"^",2,3),!
 . ; W !
 . Q
 Q
