WRITE S Q="",N=0 F I=1:1:C S N=$O(HOLD(N)) D:(I=21)!(I=42) NEXT Q:Q="^"  W !?2,$P(HOLD(N),"^",1),?8,$P(HOLD(N),"^",2)
 W !! K C,HOLD,I,N,ONE,P,Q,TWO Q
NEXT R !,"'^' TO STOP: ",Q:DTIME Q
