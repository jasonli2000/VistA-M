VECSG15 ;ATL/ACE PROGRAMMER-DICTIONARY SYSTEM
 ;;1
 ;       VARIABLE LIST
 ;
 ;       OPT         =     OPTIN NUMBER AS ENTERED BY THE USER
 ;       WORD        =     THE WORD BEING DEFINED
 ;       DEF         =     THE DEFINITION
 ;       ^VECDIC(     =     THE DICTIONARY FILE
 ;
MENU ;
 W !!,"DICTIONARY SYSTEM"
 W !!,"OPTION     FUNTION"
 W !!,"1          LOAD DICTIONARY"
 W !!,"2          PRINT DICTIONARY"
 R !!!,"ENTER OPTION NUMBER: ",OPT:DTIME G EXIT:OPT=""
 I '$T!(OPT["^") G EXIT
 I OPT<1!(OPT>2) W !,"ENTER 1 OR 2" G MENU
OPTIONS
 D LOAD:OPT=1
 D PRINT:OPT=2
 G MENU
EXIT ;
 K OPT,WORD,DEF  Q
LOAD ;
 R !!,"ENTER WORD: ",WORD:DTIME G LOADEX:WORD=""
 I '$T!(WORD["^") G LOADEX
 R !,"ENTER SHORT DEFINITION: ",DEF:DTIME
 I '$T!(DEF["^") G LOADEX
 S ^VECDIC(WORD)=DEF
 G LOAD
LOADEX         ;
 Q
PRINT ;
 S WORD=""
 F  S WORD=$O(^VECDIC(WORD)) Q:WORD=""  D OUTPUT
 Q
OUTPUT         ;
 W !!,"THE WORD IS ",WORD
 W !,"THE DEFINITION IS ",^VECDIC(WORD)
 Q
  
  
  
  
  
  
