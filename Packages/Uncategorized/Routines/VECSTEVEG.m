VECSTEVEG
 ;;1
 ;VARIABLE LIST
 ;
 ;RECNR.....RECORD NUMBER OF RECORD BEING PROCESSED
 ;INCR......CONTROL VARIABLE FO LOOP; PERCENT OF INCREASE
 ;SALARY....BASE SALARY
 ;TAB.......CURRENT TAB POSITION
 ;X.........CONTROL VARIABLE
INIT ;
 S RECNR=1
HEADING ;
 W !,?20,"SALARY TABLE",!
 FOR X=1:1:60 W "*"
 W !!,"SALARY",?10,"4%",?20,"4.5%",?30,"5%"
 W ?40,"5.5%",?50,"6%"
 W !,?10,"INCR",?20,"INCR",?30,"INCR"
 W ?40,"INCR",?50,"INCR",!!
INPUT ;
 S SALARY=$P($TEXT(DATA+RECNR),";;",2)
CHECK ;
 I SALARY="" GOTO EXIT        
CALC ;
 W !,SALARY
 S TAB=0
 FOR INCR=.04:.005:.06 DO INCRSAL
 S RECNR=RECNR+1
 GOTO INPUT
INCRSAL ;
 S TAB=TAB+10
 W ?TAB,SALARY+(SALARY*INCR)
 QUIT
EXIT ;
 K SALARY,RECNR,TAB,INCR,X
 QUIT
DATA ;
