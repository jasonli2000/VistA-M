 N I,X,ENT,INST,PARAM,TAG,VAL,RET
 F I=1:1 S X=$P($T(@("VA+"_I)),";",3) Q:X=""  D
 .S PARAM="VEJDIC "_$P(X,U),VAL=$P(X,U,2),ENT=$P(X,U,3),INST=1
 .D SET
 .Q
 D MULT ;Multi-instance parameters
 Q
 ;
SET ;  add/update parameter for PACKAGE
 N I,J,X,Y,Z,VEJD,VEJX
 ;  does it exist? - dsicxpr returns 1 or -1^msg
 S VEJD="SYS~"_PARAM_"~"_INST_"~~~Q"
 S VEJX=$$GET1^DSICXPR(,VEJD,1) Q:VEJX'?1"-1^".E
 S $P(VEJD,"~",4)=VAL,Y=$$ADD^DSICXPR(,VEJD,1)
 Q
 ;
MULT ;;DSS/LM - Initialize [multi-instance] parameters
 ; Tag MP has data in the following format:
 ; First line has PARAMETER^ENTITY
 ; Following lines have INSTANCE^VALUE, one per line
 ; End of parameter indicated by one blank line (;;)
 ; End of initialization indicated by two blank lines
 ; 
 N VEJDI,VEJDPARM,VEJDENT,VEJDINST,VEJDVAL,VEJDQUIT,VEJDX
 S (VEJDPARM,VEJDQUIT)=""
 F VEJDI=1:1 S VEJDX=$P($T(@("MP+"_VEJDI)),";",3,99) D  Q:VEJDQUIT
 .I VEJDPARM="",VEJDX="" S VEJDQUIT=1 Q  ;All parameters done
 .I VEJDPARM=""!(VEJDX="") D  Q
 ..S VEJDPARM=$P(VEJDX,U),VEJDENT=$P(VEJDX,U,2)
 ..S:VEJDENT="" VEJDENT="SYS" ;Default entity
 ..Q
 .S VEJDINST=$P(VEJDX,U),VEJDVAL=$P(VEJDX,U,2) ;Instance value pair
 .D ADD^DSICXPR(,VEJDENT_"~"_VEJDPARM_"~"_VEJDINST_"~"_VEJDVAL)
 .Q
 Q
